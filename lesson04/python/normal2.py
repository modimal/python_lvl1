def my_re_findall(line):
    result = []
    cl = 0
    substr = ''

    for x in line:
        if x.islower():
            if not substr.islower() and substr:
                if len(substr) >= 5:
                    result.append(substr[2:-2])
                substr = ''
                cl = 1
                substr += x
            else:
                cl += 1
                substr += x

                if cl > 2:
                    substr = substr[1:]
                    cl = 2
        else:
            if len(substr) >= 2:
                substr += x
            else:
                cl = 0
                substr = ''

    return result


